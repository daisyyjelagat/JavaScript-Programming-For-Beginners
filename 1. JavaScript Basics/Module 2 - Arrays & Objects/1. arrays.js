const fruits = ['apple', 'banana', 'cherry'];
const firstFruit = fruits[0]; // to access the first fruit only, in index 0
const numFruits = fruits.length; // length of an array

console.log('First fruit:', firstFruit); // Output: First fruit: apple
console.log('Number of fruits:', numFruits); // Output: Number of fruits: 3

fruits[2] = 'orange'; // changing the value at index 2
console.log('Updated fruits:', fruits); // Output: Updated fruits: [ 'apple', 'banana', 'orange' ]


